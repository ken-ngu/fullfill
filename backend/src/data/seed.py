def seed_medications(session: Session) -> None:
    """Seed medications table."""
    existing_ids = {row.id for row in session.query(Medication.id).all()}
    new_meds = [
        Medication(**{k: v for k, v in med.items()})
        for med in MEDICATIONS
        if med["id"] not in existing_ids
    ]
    if new_meds:
        session.add_all(new_meds)
        session.commit()
        print(f"✓ Seeded {len(new_meds)} medications.")
    else:
        print("✓ All medications already seeded.")


def seed_diagnoses(session: Session) -> None:
    """Seed diagnoses table and create medication-diagnosis associations."""
    existing_ids = {row.id for row in session.query(Diagnosis.id).all()}

    # Preload all medications for association
    medications_by_id = {
        med.id: med for med in session.query(Medication).all()
    }

    new_diagnoses = []

    for diag_data in DIAGNOSES:
        if diag_data["id"] in existing_ids:
            continue

        # Extract medication_ids for relationship mapping
        medication_ids = diag_data.get("medication_ids", [])

        # Create diagnosis
        diagnosis = Diagnosis(
            id=diag_data["id"],
            name=diag_data["name"],
            icd10_codes=diag_data["icd10_codes"],
            description=diag_data.get("description"),
            synonyms=diag_data["synonyms"],
            category=diag_data["category"],
        )

        # Associate medications
        for med_id in medication_ids:
            if med_id in medications_by_id:
                diagnosis.medications.append(medications_by_id[med_id])

        new_diagnoses.append(diagnosis)

    if new_diagnoses:
        session.add_all(new_diagnoses)
        session.commit()
        print(f"✓ Seeded {len(new_diagnoses)} diagnoses with medication associations.")
    else:
        print("✓ All diagnoses already seeded.")


def seed_goodrx_prices(session: Session) -> None:
    """Seed GoodRx pricing data."""
    existing_ids = {row.id for row in session.query(GoodRxPrice.id).all()}
    new_prices = [
        GoodRxPrice(**price)
        for price in GOODRX_PRICES
        if price["id"] not in existing_ids
    ]

    if new_prices:
        session.add_all(new_prices)
        session.commit()
        print(f"✓ Seeded {len(new_prices)} GoodRx prices.")
    else:
        print("✓ All GoodRx prices already seeded.")


def run_seed(database_url: Optional[str] = None) -> None:
    url = database_url or settings.database_url
    engine = create_engine(url)
    Base.metadata.create_all(engine)

    with Session(engine) as session:
        print("\n🌱 Seeding database...")
        print("=" * 50)

        # Seed in order of dependencies
        seed_medications(session)
        seed_diagnoses(session)
        seed_goodrx_prices(session)

        print("=" * 50)
        print("✅ Database seeding complete!\n")


def seed_db() -> None:
    """Alias for run_seed() for backwards compatibility."""
    run_seed()


if __name__ == "__main__":
    run_seed()
